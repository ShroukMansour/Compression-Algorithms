package textCompression;

public class Range {

    Double lower;
    Double higher;

    public Double getHigher() {
        return higher;
    }

    public Double getLower() {
        return lower;
    }

    public void setLower(Double lower) {
        this.lower = lower;
    }

    public void setHigher(Double higher) {
        this.higher = higher;
    }
}
